    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser choser=new JFileChooser();
        choser.showOpenDialog(null);
        f=choser.getSelectedFile();
        if(f==null) return;
        initialImage= Highgui.imread(f.getAbsolutePath());     
        Mat resizeimage = new Mat();
        Imgproc.resize(initialImage,resizeimage,sz);
        BufferedImage img=ImageTransformations.matToBufferedImage(resizeimage);
        ImageIcon image = new ImageIcon(img);
        jLabel1.setIcon(image);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(f==null){
           JOptionPane.showMessageDialog(null, "Load image first!");
            return; 
        }
        if(jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Low Threshold is empy!");
            return;
        }else 
            lowThreshold=Integer.parseInt(jTextField1.getText());
        
        if(jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Max Threshold is empy!");
            return;
        }else 
            maxThreshold=Integer.parseInt(jTextField2.getText());

        if(jComboBox1.getSelectedItem().equals("Gray Image")){
            initialImage= Highgui.imread(f.getAbsolutePath());
            Mat gray=new Mat();
            gray=ImageTransformations.GrayTransofrmation(initialImage);
            Mat resizeimage = new Mat();
            Imgproc.resize(gray,resizeimage,sz);
            BufferedImage img=ImageTransformations.matToBufferedImage(resizeimage);
            ImageIcon image = new ImageIcon(img);
            jLabel2.setIcon(image);
            return;
        }
        if(jComboBox1.getSelectedItem().equals("Canny Image")){
           initialImage= Highgui.imread(f.getAbsolutePath());
           Mat gray=new Mat();
           gray=ImageTransformations.GrayTransofrmation(initialImage);
           Mat canny=new Mat();
           canny=ImageTransformations.CannyTransformation(gray, lowThreshold, maxThreshold);
           Mat resizeimage = new Mat();
           Imgproc.resize(canny,resizeimage,sz);
           BufferedImage img=ImageTransformations.matToBufferedImage(resizeimage);
           ImageIcon image = new ImageIcon(img);
           jLabel2.setIcon(image);
           return;
        }
        
        if(jTextField5.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Threshold is empy!");
            return;
        }else 
            houghThreshold=Integer.parseInt(jTextField5.getText());
        
        if(jTextField6.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Min Line Lenght is empy!");
            return;
        }else 
            minLineLenght=Integer.parseInt(jTextField6.getText());
        
        if(jTextField7.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Max Line Gap is empy!");
            return;
        }else 
            maxLineGap=Integer.parseInt(jTextField7.getText());
        
        if(jComboBox1.getSelectedItem().equals("HoughLines Image")){
           initialImage= Highgui.imread(f.getAbsolutePath());
           Mat gray=new Mat();
           gray=ImageTransformations.GrayTransofrmation(initialImage);
           Mat canny=new Mat();
           canny=ImageTransformations.CannyTransformation(gray, lowThreshold, maxThreshold);
           Mat haugh=new Mat();
           haugh=ImageTransformations.DetectLines(initialImage,canny, houghThreshold, minLineLenght, maxLineGap);
           Mat resizeimage = new Mat();
           Imgproc.resize(haugh,resizeimage,sz);
           BufferedImage img=ImageTransformations.matToBufferedImage(resizeimage);
           ImageIcon image = new ImageIcon(img);
           jLabel2.setIcon(image);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

